    private void btn_userInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_userInfoActionPerformed
        try {
            /**
             * selected column  : 9 
             * selected row     : user will select
             */
            int selectedRowIndex = table_UserInfo.getSelectedRow();
            int selectedUser = (int) table_UserInfo.getModel().getValueAt(selectedRowIndex,9);
            
            UpdateUserAdminPanel selectedUserInfo = new UpdateUserAdminPanel(selectedUser);
            selectedUserInfo.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Seçim Yapılmadı");
        }
        
    }//GEN-LAST:event_btn_userInfoActionPerformed

    private void setIntoTable(String query){ // setting into the table all of user datas
        try {
            DefaultTableModel userInfoDefaultTable = (DefaultTableModel) table_UserInfo.getModel();
            userInfoDefaultTable.getDataVector().clear();
            table_UserInfo.repaint();
            
            List<UserConfig> tableContent = new DbWork().getUserInfoTable(query,tf_search.getText());
            
            Vector[] v = new Vector[tableContent.size()];
            
            for(int i =0;i<tableContent.size();i++){
                v[i] = new Vector();
                v[i].add(tableContent.get(i).getKadi());
                v[i].add(tableContent.get(i).getSifre());
                v[i].add(tableContent.get(i).getAd());
                v[i].add(tableContent.get(i).getSoyad());
                v[i].add(tableContent.get(i).getTcNo());
                v[i].add(tableContent.get(i).getCinsiyet());
                v[i].add(tableContent.get(i).getDogumTarihi());
                v[i].add(tableContent.get(i).getSehir());
                v[i].add(tableContent.get(i).getBirimAdi());
                v[i].add(tableContent.get(i).getSicilNo());
                
                
                userInfoDefaultTable.insertRow(i,v[i]);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Message: "+e.getMessage());
        }
    
        
    }
    
    
