    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
            
        /**
         * which radio button is selected?
         */
        
        if (rb_IDNo.isSelected()){
            setIntoTable("sicilNo");
            tf_search.setText("");
        }
        else if(rb_FirstName.isSelected()){
            setIntoTable("ad");
            tf_search.setText("");
        }
        else if(rb_LastName.isSelected()){
            setIntoTable("soyad");
            tf_search.setText("");
            
            
        }else if(rb_all.isSelected()){
            tf_search.setText("");
            setIntoTable(null);
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Geçersiz Giriş");
        }
    }//GEN-LAST:event_btn_searchActionPerformed

