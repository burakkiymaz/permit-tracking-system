    private void init(){ //show user information us which will we change
        String[] info = new DbWork().UserInfoForAdmin(sicilNo);
        try {
            tf_name.setText(info[0]);
            tf_surname.setText(info[1]);
            tf_TCNo.setText(info[2]);
            tf_BirthDate.setText(info[4]);
            tf_country.setText(info[5]);
            tf_username.setText(info[8]);
            tf_password.setText(info[9]);
            lbl_sicilNo.setText(info[7]);
            lbl_PermitCount.setText(info[11]);

            cmb_userType.setSelectedIndex(Integer.parseInt(info[10])-1);
            cmb_unit.setSelectedIndex(Integer.parseInt(info[6])-1);

            if ("E".equals(info[3])){
                rb_male.setSelected(true);
                rb_female.setSelected(false);
            }else{
                rb_female.setSelected(true);
                rb_male.setSelected(false);
            }
                
        } catch (Exception e) {
            System.out.println("Error: "+e.getMessage());
        }

        
    }

