    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        /**
         * saves changing
         */
        
        String[] saveData = new String[11];
        saveData[0] = tf_name.getText();
        saveData[1] = tf_surname.getText();
        saveData[2] = tf_TCNo.getText();
        
        if (rb_male.isSelected()){
            saveData[3] = "E";
        }else{
            saveData[3] = "K";
        }
        
        saveData[4] = tf_BirthDate.getText();
        saveData[5] = tf_country.getText();
        saveData[6] = Integer.toString(cmb_unit.getSelectedIndex()+1);
        saveData[7] = tf_username.getText();
        saveData[8] = tf_password.getText();
        saveData[9] = Integer.toString(cmb_userType.getSelectedIndex()+1);
        saveData[10] = Integer.toString(sicilNo);
        
        
        new DbWork().UpdateUserInfoForAdmin(saveData);
        this.dispose();
    }//GEN-LAST:event_btn_saveActionPerformed

